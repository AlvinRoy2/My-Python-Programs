# Area of Triangle
base=int(input("Enter the base of the triangle:"))
height=int(input("Enter the height of the triangle:"))
area=1/2*base*height
print("The area of triangle is",area)

# Area of Rectangle
length=int(input("Enter the length of the rectangle:"))
breath=int(input("Enter the breath of the rectangle:"))

area=length*breath
print("The area of the rectangle is",area)

# Volume of Cone
radius=int(input("Enter the radius of the cone:"))
height=int(input("Enter the height of the cone:"))
volume=1/3*3.14*(radius*radius*height)
print("The volume of the cone is",volume)

# Volume of Cylinder
radius=int(input("Enter the radius of the cylinder:"))
height=int(input("Enter the height of the cylinder:"))
volume1=3.14*radius*radius*height
print("The volume of the cylinder",volume1)

# Volume of Cuboid
length=int(input("Enter the length of the cuboid:"))
breath=int(input("Enter the breath of the cuboid:"))
height=int(input("Enter the height of the cuboid:"))
volume=length*breath*height
print("The volume of the cuboid is",volume)
