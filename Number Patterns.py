#Number Pattern
n=1
for i in range(1,5):
    print()
    for j in range(1,i+1):
        print(n,end=" ")
        n=n+1


for i in range(1,5):
    print()
    for j in range(1,6):
        print(j,end=" ")



for i in range(1,6):
    for j in range(i,6):
        print("*",end=" ")
    print()
   
